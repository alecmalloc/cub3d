#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	z;
	char	*sd;

	z = 0;
	sd = ((char *) s);
	while (z < n)
	{
		sd[z] = c;
		z++;
	}
	return (sd);
}
